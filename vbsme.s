.text
.globl  vbsme

# Your program must follow the required search pattern.  

# Preconditions:
#   1st parameter (a0) address of the first element of the dimension info (address of asize[0])
#   2nd parameter (a1) address of the first element of the frame array (address of frame[0][0])
#   3rd parameter (a2) address of the first element of the window array (address of window[0][0])
# Postconditions:	
#   result (v0) x coordinate of the block in the frame with the minimum SAD
#          (v1) y coordinate of the block in the frame with the minimum SAD


# Begin subroutine
vbsme:  
    li      $v0, 0              # reset $v0 and $V1
    li      $v1, 0

    # insert your code here
    addi    $sp, $sp, -4        # create space on the stack pointer
    sw      $ra, 0($sp)         # store the return address

    # define variables

    lw      $s0, 0($a0)         # frame row in $s0
    lw      $s1, 4($a0)         # frame column in $s1

    lw      $s2, 8($a0)         # window row in $s2
    lw      $s3, 12($a0)        # window column in $s3

    # set variables

    add		$s4, $zero, $zero   # top (0 in $s4)
    sub     $s5, $s0, $s2       # bottom (frame row - window row in $s5)
    add     $s6, $zero, $zero   # left (0 in $s6)
    sub     $s7, $s1, $s3       # right (frame column - window column in $s7)

    li      $t0, 255            # (window row * window column * 255)
    li      $t1, 0              # direction is set to 0

    mult    $t0, $s2            # mininum sum starts at maximum possible value 
    mflo    $t0                 # move from LO
    mult    $t0, $s3            # minimum sum satrts at maximum possible value
    mflo    $t0                 # move from LO
	


# jumpBack

jumpBack:
    blt     $s5, $s4, exit      # if bottom < top
    blt     $s7, $s6, exit      # if right < left

    beq     $t1, $zero, right   # if direction = 0, go right
    li      $t2, 1              # $t2 set to 1

    beq     $t1, $t2, down      # if direction = 1, go down
    li      $t2, 2              # $t2 set to 2

    beq     $t1, $t2, left      # if direction = 2, go left. else, go up.


## correct instructions for up, down, right, and left
## does not check in the order of the given pattern

# instructions for up

up:
    add     $t2, $zero, $s5     # $t2 = i is bottom
upLoop:
    blt     $t2, $s4, upExit    # if i < top exit upLoop
    add     $t3, $zero, $t2     # $t3 = i
    add     $t4, $zero, $s6     # $t4 = left
    
    jal     sad                 # jump and link sad
    
    bge     $t9, $t0, upPass    # if tempSum >= minSum then branch
    add     $t0, $t9, $zero     # minSum is tempSum
    add     $v0, $t2, $zero     # $v0 = i
    add     $v1, $s6, $zero     # $v1 = left
upPass:
    addi    $t2, $t2, -1        # increment bottom by -1 (bottom = bottom = 1)
    j       upLoop              
upExit:
    addi    $s6, $s6, 1         # left = left + 1

    li      $t1, 0              # direction = 0
    j       jumpBack




# instructions for down

down:
    add     $t2, $zero, $s4     # $t2 = i is top
downLoop:
    blt     $s5, $t2, downExit  # if bottom < i exit downLoop
    add     $t3, $zero, $t2     # $t3 = i
    add     $t4, $zero, $s7     # $t4 = right
    
    jal     sad                 # jump and link sad
    
    bge     $t9, $t0, downSkip  # if tempSum >= minSum, branch
    add     $t0, $t9, $zero     # minSum is tempSum
    add     $v0, $t2, $zero     # $v0 = i
    add     $v1, $s7, $zero     # $v1 = right
downSkip:
    addi    $t2, $t2, 1         # increment i by 1 (i = i + 1)
    j       downLoop            
downExit:
    addi    $s7, $s7, -1        # increment right by -1 (right = right - 1)

    li      $t1, 2              # direction = 2
    j       jumpBack




# instructions for left

left:
    add     $t2, $zero, $s7     # $t2 = i is right
leftLoop:
    blt     $t2, $s6, leftExit  # if i < left exit leftLoop
    add     $t3, $zero, $s5     # $t3 = bottom
    add     $t4, $zero, $t2     # $t4 = i

    jal     sad                 # jump and link sad

    bge     $t9, $t0, leftSkip  # if tempSum >= minSum, branch
    add     $t0, $t9, $zero     # minSum is tempSum
    add     $v0, $s5, $zero     # $v0 = bottom
    add     $v1, $t2, $zero     # $v1 = i
leftSkip:
    addi    $t2, $t2, -1        # increment i by 1 (i = i + 1)
    j       leftLoop            
leftExit:
    addi    $s5, $s5, -1        # increment down by -1 (down = down - 1)

    li      $t1, 3              # direction = 3
    j       jumpBack


# instructions for right

right:
    add     $t2, $zero, $s6     # $t2 = i is left
rightLoop:
    blt     $s7, $t2, exitRight # if right < i exit rightLoop
    add     $t3, $zero, $s4     # $t3 = top
    add     $t4, $zero, $t2     # $t4 = i
    
    jal     sad                 # jump and link sad
    
    bge     $t9, $t0, rightSkip # if tempSum >= minSum then branch
    add     $t0, $t9, $zero     # minSum is tempSum
    add     $v0, $s4, $zero     # $v0 = top
    add     $v1, $t2, $zero     # $v1 = i
rightSkip:
    addi    $t2, $t2, 1         # increment i by 1 (i = i + 1)
    j       rightLoop           
exitRight:
    addi    $s4, $s4, 1         # inncrement top by 1 (top = top + 1)

    li      $t1, 1              # direction = 2
    j       jumpBack



# exit

exit:
    lw      $ra, 0($sp)         # restore the original return address
    addi    $sp, $sp, 4         # restore the stack pointer
    jr      $ra                 # return

    


# sum of absolute differences function
# return will be stored in $t9

sad:
    sll     $t3, $t3, 2         # multiply y by 4 (byte index)
    sll     $t4, $t4, 2         # multiply x by 4 (byte index)
    li      $t1, 0              # set sum to 0
    mul     $t5, $t3, $s1       # y * frame column in $t3
    add     $t5, $t5, $t4       # y * frame row + x in $t5
    add     $t5, $t5, $a1       # desired index of frame in $t5
    add     $t6, $a2, $zero     # desired index of window in $t6
    li      $t3, 0              # i = 0 


# i loop

iLoop:
    slt     $t7, $t3, $s2       # $t7 = i < window row
    beq     $t7, $zero, exitiLoop   # if i >= window column, exit iLoop
    li      $t4, 0              # j = 0




# j loop

jLoop:
    slt     $t7, $t4, $s3       # $t7 = j < windowLength
    beq     $t7, $zero, exitjLoop   # if j >= windowLength, exit jLoop
    lw      $t8, 0($t5)         # $t8 is frame pixel
    lw      $t9, 0($t6)         # $t9 is window pixel
    bge     $t8, $t9, absValue
    sub     $t8, $t9, $t8
    j       endAbs

 


# absValue

absValue:
    sub     $t8, $t8, $t9       # $t8 is frame pixel - window pixel
endAbs:
    add     $t1, $t1, $t8       # add the absolute difference to sum
    addi    $t5, $t5, 4         # increment frame index
    addi    $t6, $t6, 4         # increment window index
    addi    $t4, $t4, 1         # increment j
    j       jLoop               # return to start for jLoop 



# exit j loop

exitjLoop:
    sub     $t7, $s1, $s3       # $t7 = frameLength - windowLength
    sll     $t7, $t7, 2         # multiply $t7 by 4 (byte index)
    add     $t5, $t5, $t7       # increment frame index
    addi    $t3, $t3, 1         # increment i
    j       iLoop               # return to start for iLoop



# exit i loop

exitiLoop:
    add     $t9, $t1, $zero     # store sum value in $t9
    jr      $ra  
